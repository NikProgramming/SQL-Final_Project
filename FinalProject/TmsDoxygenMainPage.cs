/// \section intro Program Introduction
/// This Program is used for the TMS (<i>Transportation Management System</i>). TMS is a critical part of the shipping and logistics industry. 
/// Allowing for the purchasing, scheduling, monitoring and billing of organizations who desire to have cargo shipped via truck or train.
/// 
///
///
/// The TMS project features to note are:
/// 
/// - <b>Account System</b>: Manages user accounts.
/// - <b>Billing System</b>: Checks to see if users have payed and generates a receipt.
/// - <b>Buyer System</b>: Allows the buyer to request contracts and create orders.
/// - <b>Carrier System</b>: Sets up what carrier is currently in use for the TMS specific orders.
/// - <b>Invoice System</b>: Creates an invoice for the users.
/// - <b>Order System</b>: Allows for customer to order items.
/// - <b>Planner System</b>: Allows for the planner to select a carrier.
/// 
/// The UI Section
/// 
/// - <b>MainWindow</b>: The main window of the program that display the delivery type, login, market place, and register for the TMS program.
/// - <b>Login</b>: used to allows <b>admins</b> to log into the program.
/// - <b>Register</b>: allows for new <b>admins</b> to be created. 
/// - <b>DeliveryTimes</b>: display the time the order is supposed to arrive.
/// - <b>MarketPlace</b>: use to allow <b>admins</b> to get new contracts.
/// 
